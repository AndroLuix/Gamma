    private void btn_okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_okActionPerformed
    
        //txt_user.getText()è un metodo che viene chiamato più di una volta
        //conviene quindi chiamarlo una sola volta e salvarlo in una variabile
        //non è stato fatto in questo esempio per mostrare la differenza con il campo JPasswordField
        
        //txt_password.getPassword() restituisce un array di char
        // String var_password = new String() converte l'array di char in stringa 
        // è una chiamata al costruttore di String
        String var_password = new String(txt_password.getPassword());
        String var_titolo = "";
        
        //lettura del file
        //al posto di "pino" e "123" considerare le variabili di user e password impostate da fonte esterna (lettura file, db, ecc)
        
        if ("pino".equals(txt_user.getText()) && "123".equals(var_password)) {
                lbl_titolo.setText("Accesso consentito");
                MenuPrinc menuprincipale = new MenuPrinc();
                this.setVisible(false);
                menuprincipale.setAlwaysOnTop(true);
                menuprincipale.setVisible(true);
                
        } else {
            //user e password inserite a video non sono corrette
            
            if (txt_user.getText().isBlank() || var_password.isBlank()) {
                //user e/o password vuote o con solo spazi
                
                if (txt_user.getText().isBlank()) {
                    var_titolo = "Inserire utente";
                    txt_user.setText("");
                }
            
                if (var_password.isBlank()) {
                    if (var_titolo.isEmpty()) {
                        var_titolo = "Inserire password";
                        txt_password.setText("");
                    } else {
                        var_titolo = var_titolo + " e password";
                        txt_user.setText("");
                        txt_password.setText("");
                    }
                }
            } else {
                //user e/o password non corrette
                if (!"pino".equals(txt_user.getText())) {
                    var_titolo = "Utente errato";
                    txt_user.setText("");
                }
                if (! "123".equals(var_password)) {
                    if (var_titolo.isEmpty()) {
                        var_titolo = "Password errata";
                        txt_password.setText("");
                    } else {
                        var_titolo = "Utente e password errati";
                        txt_user.setText("");
                        txt_password.setText("");
                    }    
                }
            }
            
            //visualizzo la finestra con errore specifico
            FinestraDialog finestra = new FinestraDialog(this, true);
            finestra.lbl_Titolo.setText(var_titolo);
            finestra.txt_Nome_Cognome.setVisible(false);
            finestra.setVisible(true);
            
        }
        
    }//GEN-LAST:event_btn_okActionPerformed

